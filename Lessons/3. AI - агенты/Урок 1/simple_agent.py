"""
  🤖 ПРОСТОЙ AI-АГЕНТ
  --------------------
  Агент = цикл + инструкция + память.
  
  Этот агент работает как продавец в киоске:
  - У него есть шпаргалка (база знаний) — словарь facts
  - Он ищет в вопросе ключевые слова
  - Отвечает только тем, что написано в шпаргалке
  - Если не знает — честно говорит "не знаю"
  
  Запускай и пробуй спрашивать!
"""

import re  # библиотека для работы с текстом (поиск слов)

# ============================================================
# 1. БАЗА ЗНАНИЙ — это память агента (словарь фактов)
#    Ключ — тема, значение — ответ агента
# ============================================================
facts = {
    "привет":     "Привет! Я твой первый AI-агент. Спрашивай что хочешь!",
    "имя":        "Меня зовут Агент-0. Я живу в этом коде.",
    "погода":     "Я не знаю погоду — у меня нет интернета. Но дома тепло!",
    "дело":       "Моё дело — отвечать на вопросы. Это всё, что я умею.",
    "пока":       "Пока! Заходи ещё, когда будет скучно.",
    "смысл":      "42. Это ответ на главный вопрос жизни, вселенной"
                  " и всего такого.",
    "робот":      "Да, я робот. Но очень дружелюбный!",
    "умный":      "Я настолько умён, насколько позволяет мой словарь фактов.",
}

# ============================================================
# 2. ФУНКЦИЯ ПОИСКА ОТВЕТА — ищет ключевые слова в вопросе
# ============================================================


def find_answer(question):
    """
    Принимает вопрос пользователя (строку).
    Ищет в нём ключевые слова из базы знаний.
    Возвращает ответ или сообщение "не знаю".

    Как работает:
    1. Переводим вопрос в нижний регистр (чтобы не путать
       'Привет' и 'привет')
    2. Перебираем все ключевые слова из словаря facts
    3. Если слово найдено в вопросе — возвращаем ответ
    4. Если ничего не нашли — говорим "не знаю"
    """
    question_lower = question.lower()  # "Привет!" -> "привет!"

    for keyword, answer in facts.items():
        # re.search — ищет слово в тексте (без учёта регистра)
        if re.search(keyword, question_lower):
            return answer

    return "Я ещё не знаю ответа на этот вопрос. Научи меня!"

# ============================================================
# 3. ГЛАВНЫЙ ЦИКЛ — сердце агента
#    while True = "делай бесконечно"
# ============================================================


def main():
    """Запускает диалог с агентом."""

    print("=" * 50)
    print("  🤖 Простой AI-агент запущен!")
    print("  Спрашивай меня что-нибудь.")
    print("  Напиши 'выход' или 'пока' чтобы выйти.")
    print("=" * 50)

    # Бесконечный цикл: агент работает, пока его не остановят
    while True:
        # Получаем вопрос от пользователя
        question = input("\nТы: ")

        # Проверяем, не хочет ли пользователь выйти
        if question.lower() in ["выход", "пока", "exit", "quit"]:
            print("Агент: Пока! Буду скучать... 😢")
            break  # break = выход из цикла

        # Ищем ответ в базе знаний
        answer = find_answer(question)

        # Выводим ответ
        print(f"Агент: {answer}")


# ============================================================
# 4. ЗАПУСК
#    Эта строчка означает: "если файл запускают напрямую —
#    выполни функцию main()"
# ============================================================
if __name__ == "__main__":
    main()
